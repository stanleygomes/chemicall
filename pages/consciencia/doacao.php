                    <div class="titulo">Procedimentos Básicos</div>
                    <div class="texto">
                        <p>
                        A coleta de sangue para doação consiste na retirada de cerca de 450ml de sangue, através do uso de material descartável, de uso único e estéril.
						No Brasil, o Ministério da Saúde exige a realização de alguns procedimentos específicos antes e depois da doação, a fim de prevenir complicações para o doador e contaminação para o receptor.
						Antes da doação, o candidato irá passar por uma entrevista de triagem clínica, na qual podem ser detectadas algumas condições adicionais que possam impedir a doação.
